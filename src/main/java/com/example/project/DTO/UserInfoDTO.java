package com.example.project.DTO;

public class UserInfoDTO {
    private String email;
    private String name;

    public UserInfoDTO() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return name;
    }

    public void setUsername(String username) {
        this.name = username;
    }
}
